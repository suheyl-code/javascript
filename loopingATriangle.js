for(let i = 0; i <= 7; i++) {
    if( i == 1) {
        console.log('#');
    }
    else if (i == 2) {
        console.log('##');
    }
    else if (i == 3) {
        console.log('###');
    }
    else if (i == 4) {
        console.log('####');
    }
    else if (i == 5) {
        console.log('#####');
    }
    else if (i == 6) {
        console.log('######');
    }
    else if (i == 7) {
        console.log('#######');
    }
    else {
        console.log('');
    }
}